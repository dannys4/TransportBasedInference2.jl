

@testset "Add tests for mixture of Gaussian kernels" begin

        M = Mixture(3)

        M.w .= [0.184018754243173;
                0.195775850480659;
                0.071604608234824;
                0.176323993535265;
                0.169941211284233;
                0.042183665772717;
                0.030869595116112;
                0.129282321333016]

        X = reshape([-0.5 -0.2; 0.1 -0.8; 2.0 1.1],(3,2))

        @test norm(log_pdf_mixture(M, X) -  [-20.597416443372648;
                                     -21.03728674466139])<1e-8

end
